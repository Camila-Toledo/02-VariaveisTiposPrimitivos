package br.com.glandata;

public class Variaveis {

	public static void main(String[] args) {

		System.out.println("Variáveis em Java");
		String nome = "Teste";

		// tipos primitivos
		boolean verifica = true;
		float numero2 = 2.5f;
		char caracter = 'a';
		int numero5 = 5;

		// tipos de classe
		Integer numero10 = 10;

		System.out.println("Resultado: " + (numero5 + numero10) + " " + nome);
	}
	
}
